    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String usr = lblUsuario.getText();
        String pwd = new String(lblSenha.getPassword());
        
        if( !usr.isEmpty())
        {
            if( !pwd.isEmpty())
            {
                //autenticar usuario e senha
                if( usr.equals("admin") && pwd.equals("admin"))
                {
                    FormMenuPrincipal f = new FormMenuPrincipal(usr);
                    
                    this.dispose();//fecha o FormLogin
                    
                    f.setVisible(true);//abre o FormMenu
                }
                else 
                {
                    JOptionPane.showMessageDialog(
                            null, "Usu치rio ou senha inv치lida.",
                            "Login", JOptionPane.WARNING_MESSAGE
                    );
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "o campo senha deve ser preenchido","Login",JOptionPane.WARNING_MESSAGE);
                txtUsuario.requestFocus();
            }
        }
        else 
        {
            JOptionPane.showMessageDialog(null, "o campo usu치rio deve ser preenchido","Login",JOptionPane.WARNING_MESSAGE);
            txtUsuario.requestFocus();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

